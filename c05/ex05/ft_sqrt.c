int	ft_sqrt(int nb)
{
	unsigned int	i;
	unsigned int	a;

	a = nb;
	i = 0;
	if (a > 0)
	{		
		while (i * i <= a)
		{
			if (i * i == a)
				return (i);
			i++;
		}
	}
	return (0);
}
